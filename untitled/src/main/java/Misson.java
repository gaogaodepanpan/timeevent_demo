public class Misson {

        private String mid;

        private String state;

        private String title;

        private String publishTime;

        private String missionInfo;

        private String label;

        private String money;

        private String existenceDate;

        private String deadline;

        private String peopleAmount;

        private String changeNumber;

        public String getMid() {
                return mid;
        }

        public void setMid(String mid) {
                this.mid = mid;
        }

        public String getState() {
                return state;
        }

        public void setState(String state) {
                this.state = state;
        }

        public String getTitle() {
                return title;
        }

        public void setTitle(String title) {
                this.title = title;
        }

        public String getPublishTime() {
                return publishTime;
        }

        public void setPublishTime(String publishTime) {
                this.publishTime = publishTime;
        }

        public String getMissionInfo() {
                return missionInfo;
        }

        public void setMissionInfo(String missionInfo) {
                this.missionInfo = missionInfo;
        }

        public String getLabel() {
                return label;
        }

        public void setLabel(String label) {
                this.label = label;
        }

        public String getMoney() {
                return money;
        }

        public void setMoney(String money) {
                this.money = money;
        }

        public String getExistenceDate() {
                return existenceDate;
        }

        public void setExistenceDate(String existenceDate) {
                this.existenceDate = existenceDate;
        }

        public String getDeadline() {
                return deadline;
        }

        public void setDeadline(String deadline) {
                this.deadline = deadline;
        }

        public String getPeopleAmount() {
                return peopleAmount;
        }

        public void setPeopleAmount(String peopleAmount) {
                this.peopleAmount = peopleAmount;
        }

        public String getChangeNumber() {
                return changeNumber;
        }

        public void setChangeNumber(String changeNumber) {
                this.changeNumber = changeNumber;
        }
}
